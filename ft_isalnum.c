#include <ctype.h>
#include <stdio.h>

int	ft_isalnum(int c)
{
	if ((c >= 49 && c <= 57) || (c >= 65 && c <= 90)
		|| (c >= 97 && c <= 122))
		return (1);
	else
		return (0);
}
